"""
The if...else Statement

This script demonstrates if...else.
"""

number = 15

if number % 2 == 0:
    print(f"{number} is an Even number.")
else:
    print(f"{number} is an Odd number.")


age = 16

if age >= 18:
    print("You can apply for a driving license.")
else:
    print("You are not eligible yet.")
