"""
The for Loop

This script demonstrates the for loop in Python.
"""

print("Numbers from 1 to 5:")

for number in range(1, 6):
    print(number)

print("\nFruits:")

fruits = ["Apple", "Banana", "Mango"]

for fruit in fruits:
    print(fruit)

print("\nCharacters:")

for letter in "Python":
    print(letter)
