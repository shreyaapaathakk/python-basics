"""
Basic Data Types in Python

This script demonstrates Python's basic built-in data types.
"""

# Integer
age = 22

# Float
height = 5.7

# String
name = "Shreya"

# Boolean
is_student = True

print("===== Values =====")
print("Age:", age)
print("Height:", height)
print("Name:", name)
print("Student:", is_student)

print("\n===== Data Types =====")
print(type(age))
print(type(height))
print(type(name))
print(type(is_student))
