"""
Type Conversion in Python

This script demonstrates how to convert one data type into another.
"""

# String to Integer
age = "22"
print("String to Integer:", int(age))

# Integer to Float
marks = 95
print("Integer to Float:", float(marks))

# Float to Integer
price = 199.99
print("Float to Integer:", int(price))

# Integer to String
roll_number = 101
print("Integer to String:", str(roll_number))

# Boolean Conversion
print("Boolean of 1:", bool(1))
print("Boolean of 0:", bool(0))
print("Boolean of 'Python':", bool("Python"))
print("Boolean of '':", bool(""))
