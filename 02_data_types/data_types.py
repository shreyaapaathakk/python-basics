"""
Python Data Types

This script demonstrates the basic built-in data types in Python.
"""

# Integer
age = 22

# Float
height = 5.7

# String
name = "Shreya Pathak"

# Boolean
is_student = True

print("=== Values ===")
print(age)
print(height)
print(name)
print(is_student)

print("\n=== Data Types ===")
print(type(age))
print(type(height))
print(type(name))
print(type(is_student))
