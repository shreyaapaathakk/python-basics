"""
Creating Strings

This script demonstrates different ways to create strings.
"""

# Single quotes
name = 'Alice'

# Double quotes
city = "London"

# Triple quotes
message = """
Welcome to Python!
Have a great learning journey.
"""

print("Name:", name)
print("City:", city)
print("Message:")
print(message)

# Empty string
empty_string = ""

print("Empty String:", empty_string)
