"""
Creating Dictionaries

This script demonstrates different ways to create dictionaries.
"""

# Creating a dictionary
student = {
    "name": "Alice",
    "age": 22,
    "course": "Computer Science"
}

print("Student:", student)

# Dictionary with different data types
person = {
    "name": "John",
    "age": 25,
    "is_student": False,
    "height": 5.9
}

print("Person:", person)

# Empty dictionary
empty_dict = {}

print("Empty Dictionary:", empty_dict)

# Creating a dictionary using dict()
country = dict(name="India", capital="New Delhi")

print("Country:", country)
