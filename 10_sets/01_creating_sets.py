"""
Creating Sets

This script demonstrates different ways to create sets.
"""

# Creating a set
fruits = {"Apple", "Banana", "Orange"}

print("Fruits:", fruits)

# Duplicate values are removed automatically
numbers = {1, 2, 3, 2, 1, 4, 5}

print("Numbers:", numbers)

# Empty set
empty_set = set()

print("Empty Set:", empty_set)

# Creating a set using set()
letters = set("Python")

print("Letters:", letters)
